luku1 = int(input("Anna kokonaisluku: ")) #pyytää ensimmäisen luvun ja muuttaa sen int
luku2 = int(input("Anna toinen kokonaisluku: ")) #pyytää toisen luvun ja muuttaa sen int

print("Lukujen summa on "+str(luku1+luku2)+".") #näyttää summan
print("Lukujen erotus on "+str(luku1-luku2)+".") #näyttää erotuksen
print("Lukujen tulo on "+str(luku1*luku2)+".") #näyttää tulon
print("Lukujen osamäärä on "+str(luku1/luku2)+".") #näyttää osamäärän
