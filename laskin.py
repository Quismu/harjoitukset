luku1 = int(input("Mikä on ensimminen luku? "));
luku2 = int(input("Mikä on toinen luku? "));

laskutoimitus = input("+, -, *, / ");

if(laskutoimitus == "+"): print(luku1 + luku2);
if(laskutoimitus == "-"): print(luku1 - luku2);
if(laskutoimitus == "*"): print(luku1 * luku2);
if(laskutoimitus == "/"): print(luku1 / luku2);
